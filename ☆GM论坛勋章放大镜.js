    let timeoutId
    function 添加悬停监听器(目标, 放大镜内容) {
        目标.addEventListener('mouseover', function () {
            if (放大镜内容) {
                clearTimeout(timeoutId); // 清除之前的隐藏任务
                显示放大镜(放大镜内容, 目标)
            }
        })
        目标.addEventListener('mouseout', () => {
            // 延迟隐藏 B
            timeoutId = setTimeout(() => {
                if (!放大镜.matches(':hover')) {
                    隐藏放大镜()
                }
            }, 100) // 延迟时间
        })
    }

